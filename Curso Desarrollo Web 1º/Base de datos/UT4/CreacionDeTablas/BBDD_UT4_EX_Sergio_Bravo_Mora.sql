CREATE TABLE ENTRENADOR (
    ID_ENTRENADOR NUMBER,
    NOMBRE VARCHAR2(25),
    APELLIDOS VARCHAR2(35),
    CONSTRAINT PK_ENTRENADOR PRIMARY KEY (ID_ENTRENADOR)
);

COMMENT ON TABLE ENTRENADOR IS 'Tabla entrenador';
COMMENT ON COLUMN ENTRENADOR.ID_ENTRENADOR IS 'Identificador de la tabla entrenador';
COMMENT ON COLUMN ENTRENADOR.NOMBRE IS 'Campo de la tabla entrenador';
COMMENT ON COLUMN ENTRENADOR.APELLIDOS IS 'Campo de la tabla entrenador';

CREATE TABLE CAMPEONATO (
    ID_CAMPEONATO NUMBER,
    NOMBRE VARCHAR2(35),
    NOMBRE_DEPORTE VARCHAR2(15),
    PAIS VARCHAR(20),
