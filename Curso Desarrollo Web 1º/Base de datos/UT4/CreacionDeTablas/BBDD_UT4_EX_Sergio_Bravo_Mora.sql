COMMENT ON COLUMN CAMPEONATO.NOMBRE_DEPORTE IS 'Campo de la tabla Campeonato';
COMMENT ON COLUMN CAMPEONATO.PAIS IS 'Campo de la tabla Campeonato';
COMMENT ON COLUMN CAMPEONATO.FECHA_FUND IS 'Campo de la tabla Campeonato';



CREATE TABLE EQUIPO (
    CODIGO_EQUIPO VARCHAR2 (10),
    NOMBRE VARCHAR2 (20),
    NOMBRE_ESTADIO VARCHAR2 (15),
    CONSTRAINT PK_EQUIPO PRIMARY KEY (CODIGO_EQUIPO)
);

COMMENT ON TABLE EQUIPO IS 'Tabla Equipo';
COMMENT ON COLUMN EQUIPO.CODIGO_EQUIPO IS 'Identificador de la tabla Equipo';
COMMENT ON COLUMN EQUIPO.NOMBRE IS 'Campo de la tabla Equipo';
COMMENT ON COLUMN EQUIPO.NOMBRE_ESTADIO IS 'Campo de la tabla Equipo';


CREATE TABLE JUGADOR (
    DNI VARCHAR2 (9),
    NOMBRE VARCHAR2 (20),
    EDAD NUMBER,
    SEXO VARCHAR2 (10),
    CODIGO_EQUIPO VARCHAR2 (10),
    CONSTRAINT PK_JUGADOR PRIMARY KEY (DNI),
    CONSTRAINT FK_EQUIPO FOREIGN KEY (CODIGO_EQUIPO) REFERENCES EQUIPO (CODIGO_EQUIPO)
);

COMMENT ON TABLE JUGADOR IS 'Tabla Jugador';
COMMENT ON COLUMN JUGADOR.DNI IS 'Identificador de la tabla Jugador';
COMMENT ON COLUMN JUGADOR.NOMBRE IS 'Campo de la tabla Jugador';
COMMENT ON COLUMN JUGADOR.EDAD IS 'Campo de la tabla Jugador';
COMMENT ON COLUMN JUGADOR.SEXO IS 'Campo de la tabla Jugador';
COMMENT ON COLUMN JUGADOR.CODIGO_EQUIPO IS 'Foreign Key de la tabla Jugador';

CREATE TABLE SELECCION (
    ID_SELECCION NUMBER,
    NOMBRE VARCHAR2(20),
    CATEGORIA VARCHAR2(10),
    NUM_JUGADORES NUMBER,
    DNI VARCHAR2(9),
    CONSTRAINT PK_SELECCION PRIMARY KEY (ID_SELECCION),
    CONSTRAINT FK_JUGADOR FOREIGN KEY (DNI) REFERENCES JUGADOR (DNI)
);

COMMENT ON TABLE SELECCION IS 'Tabla SELECCION';
COMMENT ON COLUMN SELECCION.ID_SELECCION IS 'Identificador de la tabla SELECCION';
COMMENT ON COLUMN SELECCION.CATEGORIA IS 'Campo de la tabla SELECCION';
COMMENT ON COLUMN SELECCION.NUM_JUGADORES IS 'Campo de la tabla SELECCION';
COMMENT ON COLUMN SELECCION.DNI IS 'Foreign Key de la tabla Jugador';

